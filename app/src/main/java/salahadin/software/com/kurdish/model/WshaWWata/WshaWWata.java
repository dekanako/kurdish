package salahadin.software.com.kurdish.model.WshaWWata;

public class WshaWWata
{
    private String wsh;
    private String wata;


    public WshaWWata(String wsh, String wata)
    {
        this.wsh = wsh;
        this.wata = wata;

    }

    public String getWsh()
    {
        return wsh;
    }

    public void setWsh(String wsh)
    {
        this.wsh = wsh;
    }

    public String getWata()
    {
        return wata;
    }

    public void setWata(String wata)
    {
        this.wata = wata;
    }

}
